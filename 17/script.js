/*Задача «Hello World» для функций :)
Напишите функцию min(a,b), которая возвращает меньшее из чисел a,b.
Пример вызовов:
min(2, 5) == 2
min(3, -1) == -1
min(1, 1) == 1*/
function min(a,b) {
	if (a < b) {
		return a;
	} else {
		return b;
	}
}
alert(min(2, 5));
